#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	int	i;
	int	j;

	i = 0;
	j = -1;
	while (s1[i] != '\0')
		i++;
	while (s2[++j] != '\0')
	{
		s1[i] = s2[j];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
