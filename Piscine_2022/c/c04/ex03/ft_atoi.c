int	ft_atoi(char *str)
{
	long	a;
	int		s;
	int		i;

	a = 0;
	i = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	while ((str[i] == '+') || (str[i] == '-'))
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] && (str[i] >= 48 && str[i] <= 57))
	{
		a = a * 10 + str[i] - 48;
		i++;
	}
	return (a * s);
}
