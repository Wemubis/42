int	input_check(int argc, char **argv)
{
	int	i;

	i = 0;
	while (argv[1][i] != '\0')
		i++;
	if ((argc != 2) || (i != 31)) 
		return (1);
	i = 0;
	while (i < 31)
	{
		if (argv[1][i] < '1' || argv[1][i] > '4')
			return (1);
		i += 2;
	}
	i = 1;
	while (i < 31)
	{
		if (argv[1][i] != ' ')
			return (1);
		i += 2;
	}
	return (0);
}
