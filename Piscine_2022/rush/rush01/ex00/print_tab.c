#include <unistd.h>

void	print_tab(char tab[4][4])
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (x < 4)
	{
		y = 0;
		while (y < 4)
		{
			write(1, &tab[x][y], 1);
			if (y != 3)
				write(1, " ", 1);
			y++;
		}
		x++;
		write(1, "\n", 1);
	}
}
